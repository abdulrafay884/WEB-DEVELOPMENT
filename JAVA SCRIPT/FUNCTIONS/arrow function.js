// * IT IS A BASIC ARROW FUNCTION
// ?  ADDITION
const sum = (b,c) => {
    return b+c
}
let add = sum(56,6)
console.log(add);

// ?  MULTIPLICATOION
const mul = (f,g) => {
    return f*g
}
let r = mul(5,6)
console.log(r);


// ! IT IS THE ADVANCE VERSION OF ARROW FUNCTION
// ? ADVANCE ADDITION
const plus = (m,n)=> m+n
console.log(plus(7,6));

// ? ADVANCE MULTIPLICATION
const m = (o,p)=> o*p
console.log(m(4,5));




